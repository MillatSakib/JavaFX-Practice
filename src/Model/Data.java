package Model;

/**
 *
 * @author Sakib
 */
public class Data {
    String name;
    int id;
    double salary;
    
    
    public Data(String name, int id, double salary){
    
        this.name= name;
        this.id = id;
        this.salary=salary;
    }

    public String getName() {
        return name;
    }

    public double getSalary() {
        return salary;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }
    
    
}
